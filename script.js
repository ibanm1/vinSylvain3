function calculateDistance() {
    const cityA = document.getElementById('cityA').value;
    const cityB = document.getElementById('cityB').value;

    const distanceCityAToFixedPoint = distances[cityA] || 0;
    const distanceCityBToFixedPoint = distances[cityB] || 0;

    const averageDistance = (distanceCityAToFixedPoint + distanceCityBToFixedPoint) / 2;

    const zone = getZone(averageDistance);

    const resultText = `Kms journée : ${averageDistance} km<br>Zone correspondante : ${zone}`;

    document.getElementById('result').innerHTML = resultText;
}

function getZone(distance) {
    for (const km in correspondanceKmZone) {
        if (distance <= parseInt(km)) {
            return correspondanceKmZone[km];
        }
    }
    const zones = Object.values(correspondanceKmZone);
    return zones[zones.length - 1];
}
